class User

  def initialize
    @username
    @password
    @name
    @street
    @city
    @state
    @zip
    @country
    @email
  end

  def username
    @username
  end

  def password
    @password
  end

  def name
    @name
  end

  def street
    @street
  end

  def city
    @city
  end

  def state
    @state
  end

  def zip
    @zip
  end

  def country
    @country
  end

  def email
    @email
  end

  # ______________________________

  def username= a
    @username = a
  end

  def password= b
    @password = b
  end

  def name= c
    @name = c
  end

  def street= d
    @street = d
  end

  def city= e
    @city = e
  end

  def state= i
    @state = i
  end 

  def zip= f
    @zip = f
  end

  def country= g
    @country = g
  end

  def email= h
    @email = h
  end

end
